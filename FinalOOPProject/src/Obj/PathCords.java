package Obj;

public class PathCords {
    private int xCords, yCords;

    public PathCords(int xCord, int yCord) {
        this.xCords = xCords;
        this.yCords = yCords;
    }

    public int getxCords() {
        return xCords;
    }

    public void setxCords(int xCords) {
        this.xCords = xCords;
    }

    public int getyCord() {
        return yCords;
    }

    public void setyCord(int yCord) {
        this.yCords = yCord;
    }

}